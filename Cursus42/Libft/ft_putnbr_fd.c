void ft_putnbr_fd(int n, int fd)
{
    int x;
    x = 0;
    if(n < 0)
    {
        n = n * -1;
        write (fd, "-" , 1);
    }
    while (n > 9)
    {
        putchar(n % 10 + '0');
        n = n / 10;
    }
}